package compiler


object PrintColor {
    // Reset
    const val RESET = "\u001B[0m"

    // Print Colors
    const val  RED = "\u001B[1;91m"
    const val  GREEN = "\u001B[1;92m"
    const val  YELLOW = "\u001B[1;93m"
    const val  BLUE = "\u001B[1;94m"
    const val  PURPLE = "\u001B[1;95m"
    const val  CYAN = "\u001B[1;96m"
    const val  WHITE = "\u001B[1;97m"
}

